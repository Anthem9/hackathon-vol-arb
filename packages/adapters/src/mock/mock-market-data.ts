function makeInstrument(expiry: number, strike: number, index: number): NormalizedInstrument {
  const moneyness = strike / BTC_SPOT - 1;
  const base = 0.52 - moneyness * 2.1;
  const skew = index % 3 === 0 ? 0.035 : index % 3 === 1 ? -0.015 : 0.01;
  const mid = Math.max(0.06, Math.min(0.94, base + skew));
  const width = index === 5 ? 0.18 : index === 3 ? 0.12 : 0.07;
  return {
    instrumentId: `pm-btc-above-${expiry}-${strike}`,
    venue: "Polymarket Mock",
    underlying: "BTC",
    expiry,
    strike,
    payoffType: "binary",
    direction: "above",
    quoteCurrency: "USDC",
    bid: Number(Math.max(0.01, mid - width / 2).toFixed(4)),
    ask: Number(Math.min(0.99, mid + width / 2).toFixed(4)),
    mid: Number(mid.toFixed(4)),
    bidSize: 8000 + index * 1200,
    askSize: 6500 + index * 1000,
    liquidityScore: Number((0.62 + (index % 4) * 0.08).toFixed(2)),
    settlementSource: "BTC/USD reference settlement",
    settlementRule: "Pays 1 USDC if BTC closes above strike at expiry",
    timestamp: NOW - index * 45000,
    confidenceScore: Number((0.72 + (index % 5) * 0.04).toFixed(2)),
  };
}

const instruments = expiries.flatMap((expiry) => strikes.map((strike, index) => makeInstrument(expiry.value, strike, index)));

function fairValueMap(years: number): Map<number, number> {
  return new Map(
    strikes.map((strike) => {
      const iv = 0.56 + Math.abs(strike / BTC_SPOT - 1) * 0.9;
      return [strike, Number(binaryAboveFairValue(BTC_SPOT, strike, iv, years).toFixed(4))] as const;
    }),
  );
}

const surfaces = expiries.map((expiry) =>
  buildSurface(
    expiry.label,
    expiry.value,
    instruments,
    fairValueMap(expiry.years),
    BTC_SPOT,
  ),
);

